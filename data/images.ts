// Funkcja do pobierania obrazu dla danej kategorii
export function getImageForCategory(category: string): string {
  const normalizedCategory = category.toLowerCase()

  // Sprawdź najpierw w categoryImages
  if (categoryImages[normalizedCategory as keyof typeof categoryImages]) {
    const images = categoryImages[normalizedCategory as keyof typeof categoryImages]
    return images[Math.floor(Math.random() * images.length)]
  }

  // Następnie sprawdź w sectionImages
  if (sectionImages[normalizedCategory as keyof typeof sectionImages]) {
    const images = sectionImages[normalizedCategory as keyof typeof sectionImages]
    return images[Math.floor(Math.random() * images.length)]
  }

  // Jeśli nie znaleziono, zwróć losowy obraz
  return randomImages[Math.floor(Math.random() * randomImages.length)]
}

// Funkcja do pobierania losowego obrazu
export function getRandomImage(): string {
  return randomImages[Math.floor(Math.random() * randomImages.length)]
}

// Funkcja do pobierania unikalnych obrazów dla sekcji
export function getUniqueImagesForSection(section: string, count: number): string[] {
  const sectionKey = section.toLowerCase() as keyof typeof sectionImages
  const images = sectionImages[sectionKey] || randomImages

  // Jeśli potrzebujemy więcej obrazów niż mamy w kolekcji, dodajemy losowe obrazy
  const allImages = [...images]
  while (allImages.length < count) {
    allImages.push(getRandomImage())
  }

  // Mieszamy obrazy i zwracamy określoną liczbę
  return shuffleArray(allImages).slice(0, count)
}

// Funkcja do pobierania unikalnych obrazów dla kategorii
export function getUniqueImagesForCategory(category: string, count: number): string[] {
  const normalizedCategory = category.toLowerCase() as keyof typeof categoryImages
  const images = categoryImages[normalizedCategory] || randomImages

  // Jeśli potrzebujemy więcej obrazów niż mamy w kolekcji, dodajemy losowe obrazy
  const allImages = [...images]
  while (allImages.length < count) {
    allImages.push(getRandomImage())
  }

  // Mieszamy obrazy i zwracamy określoną liczbę
  return shuffleArray(allImages).slice(0, count)
}

// Dodajemy brakującą funkcję getImages
export function getImages(category: string, count = 1): string[] {
  // Sprawdź najpierw w categoryImages
  if (categoryImages[category as keyof typeof categoryImages]) {
    return getUniqueImagesForCategory(category, count)
  }

  // Następnie sprawdź w sectionImages
  if (sectionImages[category as keyof typeof sectionImages]) {
    return getUniqueImagesForSection(category, count)
  }

  // Jeśli nie znaleziono, zwróć losowe obrazy
  return Array(count)
    .fill(0)
    .map(() => getRandomImage())
}

// Dodajemy brakującą funkcję getImage
export function getImage(category: string, index = 0): string {
  // Pobierz tablicę obrazów
  const images = getImages(category, Math.max(index + 1, 1))

  // Zwróć obraz o podanym indeksie
  return images[Math.min(index, images.length - 1)]
}

// Funkcja pomocnicza do mieszania tablicy
function shuffleArray<T>(array: T[]): T[] {
  const newArray = [...array]
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[newArray[i], newArray[j]] = [newArray[j], newArray[i]]
  }
  return newArray
}

